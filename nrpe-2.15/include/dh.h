#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0x8C,0x36,0xD7,0x39,0x27,0x32,0x33,0x6B,0x80,0xFD,0x85,0x1A,
		0x8C,0x99,0x03,0x26,0x29,0x7A,0xAE,0x90,0x10,0xB6,0xF7,0xE0,
		0x87,0xE5,0xC4,0xCD,0xAF,0xD4,0x69,0xA5,0x88,0x97,0x8E,0x7B,
		0xE8,0x1A,0x28,0xCE,0x62,0x6A,0x2B,0xC8,0xF2,0x2E,0x96,0x3A,
		0x50,0xDB,0xE9,0xA6,0x83,0x45,0x0E,0x9D,0xF6,0x57,0xAD,0xBA,
		0x17,0xA2,0x21,0x43,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
